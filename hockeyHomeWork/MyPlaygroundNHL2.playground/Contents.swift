func playGame(teamOne: Team, teamTwo: Team) {

    let teamOneRating = teamOne.teamRating()
    let teamTwoRating = teamTwo.teamRating()
    
    let rand = -50 + Int(random() % 100)

    if  teamOneRating + rand > teamTwoRating  {
        teamOne.wonGame()
        teamTwo.lostGame()
    } else {
        teamOne.lostGame()
        teamTwo.wonGame()
    }
    
}


